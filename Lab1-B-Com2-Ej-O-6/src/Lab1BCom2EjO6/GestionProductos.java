    private void cargarCombo() {
        jcbCat.addItem(Rubro.LIMPIEZA);
        jcbCat.addItem(Rubro.COMESTIBLES);
        jcbCat.addItem(Rubro.PERFUMERIA);
    }

    public boolean busquedaCodigo() {

        boolean encontrado = false;
        int i = 0;

        for (Productos prod : Menu.listaProductos) {
            String codigo = String.valueOf(prod.getCodigo());
            if (codigo.equals(jtCod.getText())) {
                JOptionPane.showMessageDialog(this, "Producto ya registrado");
                encontrado = true;

                jtDes.setText(prod.getDescripcion());
                double pre = prod.getPrecio();
                String preGua = String.valueOf(pre);
                jtPre.setText(preGua);
                int sto = prod.getStock();
                String stoGua = String.valueOf(sto);
                jtSto.setText(stoGua);
                jbEli.setEnabled(true);

                break;
            }
        }
        // Mostrar un mensaje si no se encuentra el producto
        if (!encontrado) {
            JOptionPane.showMessageDialog(this, "Producto no registrado");
            jtDes.setEnabled(true);
            jtPre.setEnabled(true);
            jtSto.setEnabled(true);
            jcbCat.setEnabled(true);
            jbGua.setEnabled(true);
            jbNue.setEnabled(false);
            jbEli.setEnabled(false);
            limpiarCampos();
        }
        return encontrado;
    }

    public static void agregarProductoAlArreglo(Productos producto) {
        Menu.listaProductos.add(producto);
    }

    public void limpiarCampos(){
        jtCod.setText("");
        jtDes.setText("");
        jtPre.setText("");
        jtSto.setText("");
    }
    
    public Productos agregarProducto() {
        int c = Integer.parseInt(jtCod.getText());
        String d = jtDes.getText();
        double p = Double.parseDouble(jtPre.getText());
        int s = Integer.parseInt(jtSto.getText());
        Rubro cat = (Rubro) jcbCat.getSelectedItem();
        Productos prod = new Productos(c, d, p, s, cat);
        Menu.listaProductos.add(prod);
        return prod;
    }

    public void habilitarCampos() {
        jtDes.setEnabled(true);
        jtPre.setEnabled(true);
        jtSto.setEnabled(true);
        jcbCat.setEnabled(false);
        jbGua.setEnabled(true);
    }

