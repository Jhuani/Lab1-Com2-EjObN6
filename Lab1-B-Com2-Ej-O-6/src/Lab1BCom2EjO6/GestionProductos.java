    private void jbEliActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jbEliActionPerformed
        // TODO add your handling code here:
        
        int codigo = Integer.parseInt(jtCod.getText());

    // Buscar el producto en la lista de productos y eliminarlo
    for (Productos prod : Menu.listaProductos) {
        if (prod.getCodigo() == codigo) {
            Menu.listaProductos.remove(prod);
            JOptionPane.showMessageDialog(this, "Producto eliminado con éxito");
            limpiarCampos();
            return; // Salir del bucle una vez que se elimine el producto
        }
    }

    // Si no se encuentra el producto, mostrar un mensaje de error
    JOptionPane.showMessageDialog(this, "Producto no encontrado");
        
    }//GEN-LAST:event_jbEliActionPerformed


